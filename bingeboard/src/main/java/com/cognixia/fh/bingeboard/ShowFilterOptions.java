package com.cognixia.fh.bingeboard;

/**
 * ShowFilterOptions is an enumeration that defines the different options available for filtering shows.
 * It includes options to filter by progress, view all shows, and filter by various attributes such as 
 * director, writer, actor, genre, and TV network.
 */
public enum ShowFilterOptions {
    BY_PROGRESS,
    VIEW_ALL,
    BY_DIRECTOR,
    BY_WRITER,
    BY_ACTOR,
    BY_GENRE,
    BY_TV_NETWORK
}
